package com.ak.dependency.model;

public class BalanceInfo {
	
	private String avalBal;
	private String debtBal;
	
	public String getAvalBal() {
		return avalBal;
	}
	public void setAvalBal(String avalBal) {
		this.avalBal = avalBal;
	}
	public String getDebtBal() {
		return debtBal;
	}
	public void setDebtBal(String debtBal) {
		this.debtBal = debtBal;
	}
	
	

}
